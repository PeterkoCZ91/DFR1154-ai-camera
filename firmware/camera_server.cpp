// Send log over UDP to Raspberry Pi with error handling
void send_udp_log(const char* level, const char* message) {
    if (!udp_logging_enabled || WiFi.status() != WL_CONNECTED) {
        return;  // Skip if no WiFi or logging is off
    }

    // Mutex is created in startCameraServer(), skip if not ready yet
    if (udp_mutex == NULL) return;

    char log_message[512];
    unsigned long uptime_ms = millis();

    // Format: [ESP32][uptime][level] message
    int written = snprintf(log_message, sizeof(log_message), "[ESP32][%lu][%s] %s", uptime_ms, level, message);

    // Check buffer overflow
    if (written >= sizeof(log_message)) {
        Serial.println("⚠️ UDP log message truncated!");
        log_message[sizeof(log_message) - 1] = '\0';  // Ensure null termination
    }

    if (udp_mutex && xSemaphoreTake(udp_mutex, pdMS_TO_TICKS(10))) {
        udpLog.beginPacket(RASPBERRY_PI_IP, UDP_LOG_PORT);
        udpLog.write((const uint8_t*)log_message, strlen(log_message));

        // Check if packet was sent successfully
        if (!udpLog.endPacket()) {
            static unsigned long last_error_log = 0;
            if (millis() - last_error_log > 10000) {  // Log error max every 10s
                Serial.println("⚠️ Failed to send UDP log packet");
                last_error_log = millis();
            }
        }
        xSemaphoreGive(udp_mutex);
    }
}

// HTTP Basic Auth helper
// In LAB_MODE: always returns ESP_OK (no auth check)
// In production: validates Authorization header against config.http_user:config.http_pass
static esp_err_t check_basic_auth(httpd_req_t *req) {
#if LAB_MODE
    return ESP_OK;  // No auth in lab mode
#else
    char auth_header[256] = {0};
    esp_err_t err = httpd_req_get_hdr_value_str(req, "Authorization", auth_header, sizeof(auth_header));

    if (err != ESP_OK) {
        httpd_resp_set_status(req, "401 Unauthorized");
        httpd_resp_set_type(req, "text/plain");
        httpd_resp_set_hdr(req, "WWW-Authenticate", "Basic realm=\"ESP32-Cam\"");
        httpd_resp_send(req, "Authentication required", HTTPD_RESP_USE_STRLEN);
        return ESP_FAIL;
    }

    // Expect "Basic <base64>"
    if (strncmp(auth_header, "Basic ", 6) != 0) {
        httpd_resp_set_status(req, "401 Unauthorized");
        httpd_resp_set_type(req, "text/plain");
        httpd_resp_set_hdr(req, "WWW-Authenticate", "Basic realm=\"ESP32-Cam\"");
        httpd_resp_send(req, "Invalid auth scheme", HTTPD_RESP_USE_STRLEN);
        return ESP_FAIL;
    }

    const char* b64_creds = auth_header + 6;
    unsigned char decoded[128];
    size_t decoded_len = 0;
    int ret = mbedtls_base64_decode(decoded, sizeof(decoded) - 1, &decoded_len,
                                     (const unsigned char*)b64_creds, strlen(b64_creds));
    if (ret != 0) {
        httpd_resp_set_status(req, "401 Unauthorized");
        httpd_resp_set_type(req, "text/plain");
        httpd_resp_set_hdr(req, "WWW-Authenticate", "Basic realm=\"ESP32-Cam\"");
        httpd_resp_send(req, "Invalid credentials", HTTPD_RESP_USE_STRLEN);
        return ESP_FAIL;
    }
    decoded[decoded_len] = '\0';

    // Build expected "user:pass"
    String expected = config.http_user + ":" + config.http_pass;
    if (expected.equals((const char*)decoded)) {
        return ESP_OK;
    }

    httpd_resp_set_status(req, "401 Unauthorized");
    httpd_resp_set_type(req, "text/plain");
    httpd_resp_set_hdr(req, "WWW-Authenticate", "Basic realm=\"ESP32-Cam\"");
    httpd_resp_send(req, "Invalid credentials", HTTPD_RESP_USE_STRLEN);
    return ESP_FAIL;
#endif
}

