static esp_err_t save_frame_handler(httpd_req_t *req) {
    if (check_basic_auth(req) != ESP_OK) return ESP_FAIL;

    // Check if SD card is mounted
    if(!SD.cardSize()) {
        httpd_resp_send_500(req);
        return ESP_FAIL;
    }

    // Allocate buffer for filename
    char filepath[64];
    time_t now;
    struct tm timeinfo;
    time(&now);
    localtime_r(&now, &timeinfo);
    
    // Create filename based on timestamp
    // Note: If NTP is not synced, time will be from boot
    strftime(filepath, sizeof(filepath), "/%Y%m%d_%H%M%S.jpg", &timeinfo);
    
    // Check content length (limit to 500KB to be safe)
    if (req->content_len > 500 * 1024) {
        Serial.println("Frame too large");
        httpd_resp_send_500(req);
        return ESP_FAIL;
    }
    
    // Allocate buffer
    char *buf = (char *)malloc(req->content_len);
    if (!buf) {
        Serial.println("Failed to allocate buffer");
        httpd_resp_send_500(req);
        return ESP_FAIL;
    }
    
    // Receive data with retry limit
    int ret = 0;
    int remaining = req->content_len;
    int timeout_retries = 0;
    const int MAX_TIMEOUT_RETRIES = 10;
    while (remaining > 0) {
        int recved = httpd_req_recv(req, buf + ret, remaining);
        if (recved <= 0) {
            if (recved == HTTPD_SOCK_ERR_TIMEOUT) {
                timeout_retries++;
                if (timeout_retries >= MAX_TIMEOUT_RETRIES) {
                    Serial.println("❌ save_frame: Too many timeouts");
                    free(buf);
                    return ESP_FAIL;
                }
                continue;
            }
            free(buf);
            return ESP_FAIL;
        }
        timeout_retries = 0;  // Reset on successful read
        ret += recved;
        remaining -= recved;
    }
    
    // Write to SD card
    File file = SD.open(filepath, FILE_WRITE);
    if (!file) {
        Serial.println("Failed to open file for writing");
        free(buf);
        httpd_resp_send_500(req);
        return ESP_FAIL;
    }
    
    file.write((uint8_t*)buf, req->content_len);
    file.close();
    free(buf);
    
    Serial.printf("Saved frame to %s\n", filepath);
    httpd_resp_send(req, "Frame saved", HTTPD_RESP_USE_STRLEN);
    return ESP_OK;
}

// --- SD Card Auto-Recording Helpers ---
// Called from motionTask (camera_capture.cpp) to start/stop auto-recording

bool startSDRecording() {
    if (!SD.cardSize()) return false;
    if (!recordingMutex) return false;

    if (xSemaphoreTake(recordingMutex, pdMS_TO_TICKS(500)) != pdTRUE) return false;

    if (is_recording) {
        xSemaphoreGive(recordingMutex);
        return true; // Already recording
    }

    // Storage rotation: delete oldest files if SD is too full
    uint64_t total = SD.totalBytes();
    uint64_t used = SD.usedBytes();
    if (total > 0) {
        int usage_pct = (int)(used * 100 / total);
        if (usage_pct >= config.sd_max_usage_percent) {
            Serial.printf("💾 SD usage %d%% >= %d%%, rotating oldest files...\n",
                          usage_pct, config.sd_max_usage_percent);
            // Find and delete oldest file (by name — files use timestamps)
            int rotated = 0;
            while (usage_pct >= config.sd_max_usage_percent - 5 && rotated < 20) {
                File root = SD.open("/");
                if (!root) break;
                String oldest_name = "";
                File entry = root.openNextFile();
                while (entry) {
                    if (!entry.isDirectory()) {
                        String name = "/" + String(entry.name());
                        if (oldest_name.isEmpty() || name < oldest_name) {
                            oldest_name = name;
                        }
                    }
                    entry.close();
                    entry = root.openNextFile();
                }
                root.close();
                if (oldest_name.isEmpty()) break;
                size_t fsize = 0;
                File f = SD.open(oldest_name);
                if (f) { fsize = f.size(); f.close(); }
                if (SD.remove(oldest_name.c_str())) {
                    rotated++;
                    Serial.printf("  🗑️ Rotated: %s (%u KB)\n", oldest_name.c_str(), (unsigned)(fsize / 1024));
                } else {
                    break;
                }
                used = SD.usedBytes();
                usage_pct = (int)(used * 100 / total);
            }
            if (rotated > 0) {
                Serial.printf("  💾 Rotated %d files, now %d%% used\n", rotated, usage_pct);
            }
        }
    }

    // Generate filename with NTP timestamp
    char filename[64];
    time_t now;
    struct tm timeinfo;
    time(&now);
    localtime_r(&now, &timeinfo);
    strftime(filename, sizeof(filename), "/rec_%Y%m%d_%H%M%S.avi", &timeinfo);

    recordingFile = SD.open(filename, FILE_WRITE);
    if (recordingFile) {
        // Initialize AVI writer with current resolution and configured fps
        sensor_t* s = esp_camera_sensor_get();
        uint16_t w = 640, h = 480; // VGA default
        if (s) {
            // Map framesize to pixels (VGA=640x480 is the common recording resolution)
            switch (s->status.framesize) {
                case FRAMESIZE_QVGA: w = 320; h = 240; break;
                case FRAMESIZE_VGA:  w = 640; h = 480; break;
                case FRAMESIZE_SVGA: w = 800; h = 600; break;
                case FRAMESIZE_XGA:  w = 1024; h = 768; break;
                default: w = 640; h = 480; break;
            }
        }
        int fps = config.sd_record_fps > 0 ? config.sd_record_fps : 10;
        if (!aviWriter.begin(recordingFile, w, h, fps, I2S_SAMPLE_RATE)) {
            Serial.println("❌ AVI header write failed");
            recordingFile.close();
            xSemaphoreGive(recordingMutex);
            return false;
        }

        strncpy(lastRecordingFilename, filename, sizeof(lastRecordingFilename) - 1);
        lastRecordingFilename[sizeof(lastRecordingFilename) - 1] = '\0';
        is_recording = true;
        Serial.printf("💾 AVI recording started: %s (%dx%d @%dfps)\n", filename, w, h, fps);
        xSemaphoreGive(recordingMutex);
        return true;
    }

    Serial.println("❌ Auto-recording: failed to open file");
    xSemaphoreGive(recordingMutex);
    return false;
}

void stopSDRecording() {
    if (!recordingMutex) return;
    if (!is_recording) return;

    if (xSemaphoreTake(recordingMutex, pdMS_TO_TICKS(500)) == pdTRUE) {
        is_recording = false;
        xSemaphoreGive(recordingMutex);
        // Wait for recordingTask to finish its current write
        vTaskDelay(pdMS_TO_TICKS(200));
        if (xSemaphoreTake(recordingMutex, pdMS_TO_TICKS(2000)) == pdTRUE) {
            aviWriter.end();
            recordingFile.close();
            Serial.printf("💾 AVI recording stopped: %u frames, %u audio chunks\n",
                aviWriter.getVideoFrames(), aviWriter.getAudioChunks());
            xSemaphoreGive(recordingMutex);
        }
    }
}

// --- SD Download Handler ---
static esp_err_t sd_download_handler(httpd_req_t *req) {
    set_cors_headers(req);

    if (!SD.cardSize()) {
        httpd_resp_send_err(req, HTTPD_404_NOT_FOUND, "No SD card");
        return ESP_FAIL;
    }

    // Parse ?file= query parameter
    char query[128] = {0};
    char filepath[96] = {0};
    if (httpd_req_get_url_query_str(req, query, sizeof(query)) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "Missing ?file= parameter");
        return ESP_FAIL;
    }
    if (httpd_query_key_value(query, "file", filepath, sizeof(filepath)) != ESP_OK) {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "Missing file parameter");
        return ESP_FAIL;
    }

    // Security: prevent path traversal
    if (strstr(filepath, "..") != NULL) {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "Invalid path");
        return ESP_FAIL;
    }

    // Ensure path starts with /
    String path = filepath;
    if (!path.startsWith("/")) path = "/" + path;

    File file = SD.open(path, FILE_READ);
    if (!file || file.isDirectory()) {
        if (file) file.close();
        httpd_resp_send_err(req, HTTPD_404_NOT_FOUND, "File not found");
        return ESP_FAIL;
    }

    // Set content type based on extension
    if (path.endsWith(".jpg") || path.endsWith(".jpeg")) {
        httpd_resp_set_type(req, "image/jpeg");
    } else if (path.endsWith(".mjpeg")) {
        httpd_resp_set_type(req, "application/octet-stream");
    } else {
        httpd_resp_set_type(req, "application/octet-stream");
    }

    // Content-Disposition: attachment for download
    String disposition = "attachment; filename=\"" + path.substring(path.lastIndexOf('/') + 1) + "\"";
    httpd_resp_set_hdr(req, "Content-Disposition", disposition.c_str());

    // Stream file in 4KB chunks
    size_t total = file.size();
    char chunk[4096];
    size_t sent = 0;
    while (sent < total) {
        size_t to_read = (total - sent > sizeof(chunk)) ? sizeof(chunk) : (total - sent);
        size_t read = file.read((uint8_t*)chunk, to_read);
        if (read == 0) break;
        if (httpd_resp_send_chunk(req, chunk, read) != ESP_OK) {
            file.close();
            return ESP_FAIL;
        }
        sent += read;
        // Reset WDT for large files
        if (sent % (64 * 1024) == 0) esp_task_wdt_reset();
    }
    file.close();

    // End chunked response
    httpd_resp_send_chunk(req, NULL, 0);
    Serial.printf("💾 SD download: %s (%u KB)\n", path.c_str(), (unsigned)(total / 1024));
    return ESP_OK;
}

// Handler: SD Card — list files (GET) and clear all (POST)
static void deleteAllFiles(fs::FS &fs, const char *dirname, int &count) {
    // Loop until directory is empty (handles dirs with thousands of files)
    String dirStr = String(dirname);
    if (!dirStr.endsWith("/") && dirStr != "/") dirStr += "/";

    while (true) {
        File root = fs.open(dirname);
        if (!root || !root.isDirectory()) { root.close(); return; }

        File entry = root.openNextFile();
        if (!entry) { root.close(); return; } // Directory empty, done

        // Build full path: dirname + "/" + entry name
        // file.name() on ESP32 SD returns just the name (not full path)
        String name = String(entry.name());
        // Strip any leading path components — just keep the filename
        int lastSlash = name.lastIndexOf('/');
        if (lastSlash >= 0) name = name.substring(lastSlash + 1);
        String path = (dirStr == "/") ? "/" + name : dirStr + name;

        bool dir = entry.isDirectory();
        entry.close();
        root.close(); // Close dir before modifying (important for FAT)

        Serial.printf("  [%s] %s\n", dir ? "DIR" : "FILE", path.c_str());

        if (dir) {
            deleteAllFiles(fs, path.c_str(), count);
            if (!fs.rmdir(path.c_str())) {
                Serial.printf("  FAIL rmdir: %s (skipping)\n", path.c_str());
                return;
            }
        } else {
            if (fs.remove(path.c_str())) {
                count++;
                if (count % 200 == 0) Serial.printf("  🗑️ %d files deleted...\n", count);
            } else {
                Serial.printf("  FAIL rm: %s (skipping)\n", path.c_str());
                return;
            }
        }
    }
}

static esp_err_t sd_list_handler(httpd_req_t *req) {
    set_cors_headers(req);

    if (!SD.cardSize()) {
        httpd_resp_set_type(req, "application/json");
        httpd_resp_send(req, "{\"error\":\"no_sd_card\"}", HTTPD_RESP_USE_STRLEN);
        return ESP_OK;
    }

    File root = SD.open("/");
    if (!root) {
        httpd_resp_set_type(req, "application/json");
        httpd_resp_send(req, "{\"error\":\"cannot_open_root\"}", HTTPD_RESP_USE_STRLEN);
        return ESP_OK;
    }

    httpd_resp_set_type(req, "text/plain");

    String listing;
    File entry = root.openNextFile();
    int count = 0;
    while (entry && count < 200) {
        listing += entry.isDirectory() ? "[DIR] " : "";
        listing += String(entry.name());
        if (!entry.isDirectory()) {
            listing += " (" + String(entry.size() / 1024) + " KB)";
        }
        listing += "\n";
        entry.close();
        count++;
        entry = root.openNextFile();
    }
    root.close();

    if (listing.isEmpty()) listing = "(empty)\n";
    listing += "\nTotal: " + String(SD.usedBytes() / (1024*1024)) + " MB used / " +
               String(SD.totalBytes() / (1024*1024)) + " MB total\n";

    httpd_resp_send(req, listing.c_str(), HTTPD_RESP_USE_STRLEN);
    return ESP_OK;
}

static esp_err_t sd_clear_handler(httpd_req_t *req) {
    if (check_basic_auth(req) != ESP_OK) return ESP_FAIL;
    set_cors_headers(req);
    httpd_resp_set_type(req, "application/json");

    if (!SD.cardSize()) {
        httpd_resp_send(req, "{\"error\":\"no_sd_card\"}", HTTPD_RESP_USE_STRLEN);
        return ESP_OK;
    }

    Serial.println("🗑️ SD card clear requested...");
    uint64_t before = SD.usedBytes();
    int count = 0;
    deleteAllFiles(SD, "/", count);
    uint64_t after = SD.usedBytes();
    Serial.printf("🗑️ SD cleared: %d files, %llu MB freed\n", count, (before - after) / (1024 * 1024));

    char json[128];
    snprintf(json, sizeof(json), "{\"files_deleted\":%d,\"freed_mb\":%llu,\"remaining_mb\":%llu}",
             count, (before - after) / (1024 * 1024), after / (1024 * 1024));
    httpd_resp_send(req, json, HTTPD_RESP_USE_STRLEN);
    return ESP_OK;
}

