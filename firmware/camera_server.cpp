// Generic Video Stream Handler to reduce duplication
static esp_err_t generic_video_stream_handler(httpd_req_t *req, int *active_clients, int max_clients, const char* stream_name, bool limit_fps) {
    esp_err_t res = ESP_OK;
    char part_buf[64];

    // Client limit check (atomic read)
    int current_count = __atomic_load_n(active_clients, __ATOMIC_SEQ_CST);
    if (current_count >= max_clients) {
        Serial.printf("⚠️  %s: Max clients reached, rejecting new connection\n", stream_name);
        char log_msg[128];
        snprintf(log_msg, sizeof(log_msg), "%s: Max clients reached, connection rejected", stream_name);
        send_udp_log("WARN", log_msg);
        httpd_resp_set_status(req, "429 Too Many Requests");
        httpd_resp_send(req, "Too many clients connected", HTTPD_RESP_USE_STRLEN);
        return ESP_FAIL;
    }

    __atomic_fetch_add(active_clients, 1, __ATOMIC_SEQ_CST);
    current_count = __atomic_load_n(active_clients, __ATOMIC_SEQ_CST);
    Serial.printf("✅ %s connected (clients: %d/%d)\n", stream_name, current_count, max_clients);

    char connect_msg[128];
    snprintf(connect_msg, sizeof(connect_msg), "%s connected (clients: %d/%d)",
             stream_name, *active_clients, max_clients);
    send_udp_log("INFO", connect_msg);

    res = httpd_resp_set_type(req, _STREAM_CONTENT_TYPE);
    if (limit_fps) {
        set_cors_headers(req);
        httpd_resp_set_hdr(req, "X-Framerate", "30");
    } else {
        httpd_resp_set_hdr(req, "Access-Control-Allow-Origin", "*");
    }
    
    httpd_resp_set_hdr(req, "Content-Disposition", "inline; filename=stream.mjpeg");

    if(res != ESP_OK) {
        __atomic_fetch_sub(active_clients, 1, __ATOMIC_SEQ_CST);
        return res;
    }

    // Set socket send timeout
    setStreamSocketTimeout(req, 500);

    // Zero-Copy Ring Buffer Implementation
    // No local buffer allocation needed!

    unsigned long frame_count = 0;
    unsigned long my_last_timestamp = 0;

    while(true) {
        // Wait for new frame
        bool new_frame = false;
        unsigned long wait_start = millis();
        int frame_idx = -1;

        while (!new_frame) {
            // Check timeout (if no frame for 2s, abort)
            if (millis() - wait_start > 2000) {
                break;
            }
            
            // Check if new frame available in Ring Buffer (CAS-safe reader)
            int idx = getLatestFrameIndex();
            if (idx >= 0 && frameBuffers[idx].timestamp > my_last_timestamp) {
                if (acquireFrameReader(idx)) {
                    frame_idx = idx;
                    my_last_timestamp = frameBuffers[idx].timestamp;
                    new_frame = true;
                }
            }
            
            if (!new_frame) {
                vTaskDelay(5); // Wait a bit
            }
        }

        if (!new_frame) {
            Serial.println("⚠️ MJPEG: No frame from capture task");
            res = ESP_FAIL;
            break;
        }

        // Send boundary
        res = httpd_resp_send_chunk(req, _STREAM_BOUNDARY, strlen(_STREAM_BOUNDARY));
        if(res == ESP_OK) {
            // Send header with length
            size_t hlen = snprintf((char *)part_buf, 64, _STREAM_PART, frameBuffers[frame_idx].len);
            res = httpd_resp_send_chunk(req, (const char *)part_buf, hlen);
        }
        
        if(res == ESP_OK) {
            // Send frame data directly from Ring Buffer (Zero-Copy!)
            res = httpd_resp_send_chunk(req, (const char *)frameBuffers[frame_idx].data, frameBuffers[frame_idx].len);
        }

        // Release frame reader reference
        releaseFrameReader(frame_idx);

        if(res != ESP_OK) {
            Serial.println("Stream send failed (timeout or disconnect)");
            // FIXED: Force close socket on error to prevent socket leak
            // Half-open sockets can accumulate after timeouts, exhausting available sockets
            int sockfd = httpd_req_to_sockfd(req);
            if (sockfd >= 0) {
                httpd_sess_trigger_close(stream_httpd, sockfd);
            }
            break;
        }
        
        if (limit_fps) {
            // Precise timing handled by capture task, but we can yield here
            // vTaskDelay(pdMS_TO_TICKS(1));
            // Actually, for detection stream we might want to limit rate if client is slow
             vTaskDelay(pdMS_TO_TICKS(33));
        }

        // Reset WDT periodically (every 30 frames = ~1 second at 30 FPS)
        if (frame_count % 30 == 0) {
            esp_task_wdt_reset();
        }

        frame_count++;
    }
    
    __atomic_fetch_sub(active_clients, 1, __ATOMIC_SEQ_CST);
    int remaining = __atomic_load_n(active_clients, __ATOMIC_SEQ_CST);
    Serial.printf("🔌 %s disconnected (clients: %d/%d, frames: %lu)\n", stream_name, remaining, max_clients, frame_count);

    char disconnect_msg[128];
    snprintf(disconnect_msg, sizeof(disconnect_msg), "%s disconnected (clients: %d/%d, frames: %lu)",
             stream_name, remaining, max_clients, frame_count);
    send_udp_log("INFO", disconnect_msg);
    
    return res;
}

