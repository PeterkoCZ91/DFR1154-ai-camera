// Helper: set send timeout on HTTP stream socket
static void setStreamSocketTimeout(httpd_req_t *req, int timeout_ms = 500) {
    int sockfd = httpd_req_to_sockfd(req);
    struct timeval send_timeout;
    send_timeout.tv_sec = timeout_ms / 1000;
    send_timeout.tv_usec = (timeout_ms % 1000) * 1000;
    if (setsockopt(sockfd, SOL_SOCKET, SO_SNDTIMEO, &send_timeout, sizeof(send_timeout)) < 0) {
        Serial.println("Failed to set socket send timeout");
    }
}

// Helper function to set CORS headers
static void set_cors_headers(httpd_req_t *req) {
    httpd_resp_set_hdr(req, "Access-Control-Allow-Origin", "*");
    httpd_resp_set_hdr(req, "Access-Control-Allow-Methods", "GET, POST, OPTIONS");
    httpd_resp_set_hdr(req, "Access-Control-Allow-Headers", "Content-Type, Authorization");
    httpd_resp_set_hdr(req, "Access-Control-Max-Age", "86400"); // Cache preflight for 24 hours
}

