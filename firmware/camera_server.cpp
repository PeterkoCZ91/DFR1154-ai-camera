// HTTP OTA Update handler — POST /update with raw firmware binary body
static esp_err_t ota_update_handler(httpd_req_t *req) {
    if (check_basic_auth(req) != ESP_OK) return ESP_FAIL;

    Serial.printf("OTA: Receiving firmware (%d bytes)...\n", req->content_len);

    if (req->content_len <= 0 || req->content_len > 6000000) {
        httpd_resp_send_err(req, HTTPD_400_BAD_REQUEST, "Invalid firmware size");
        return ESP_FAIL;
    }

    // Suspend camera capture to free memory and prevent crashes
    if (captureTaskHandle) {
        vTaskSuspend(captureTaskHandle);
        Serial.println("OTA: Camera capture suspended");
    }

    if (!Update.begin(req->content_len)) {
        Serial.printf("OTA: Update.begin failed: %s\n", Update.errorString());
        if (captureTaskHandle) vTaskResume(captureTaskHandle);
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "OTA begin failed");
        return ESP_FAIL;
    }

    // Read and write firmware in chunks
    char buf[4096];
    int remaining = req->content_len;
    int written = 0;

    while (remaining > 0) {
        int recv_len = httpd_req_recv(req, buf, (remaining < sizeof(buf)) ? remaining : sizeof(buf));
        if (recv_len <= 0) {
            if (recv_len == HTTPD_SOCK_ERR_TIMEOUT) continue;
            Serial.println("OTA: Receive error");
            Update.abort();
            if (captureTaskHandle) vTaskResume(captureTaskHandle);
            httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "Receive error");
            return ESP_FAIL;
        }

        if (Update.write((uint8_t*)buf, recv_len) != recv_len) {
            Serial.printf("OTA: Write error: %s\n", Update.errorString());
            Update.abort();
            if (captureTaskHandle) vTaskResume(captureTaskHandle);
            httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "Write error");
            return ESP_FAIL;
        }

        remaining -= recv_len;
        written += recv_len;

        // Progress every 10%
        if (written % (req->content_len / 10 + 1) < recv_len) {
            Serial.printf("OTA: %d%% (%d/%d bytes)\n", written * 100 / req->content_len, written, req->content_len);
            esp_task_wdt_reset();
        }
    }

    if (!Update.end(true)) {
        Serial.printf("OTA: Finalize error: %s\n", Update.errorString());
        if (captureTaskHandle) vTaskResume(captureTaskHandle);
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "OTA finalize failed");
        return ESP_FAIL;
    }

    Serial.println("OTA: Update successful! Rebooting...");
    httpd_resp_set_type(req, "application/json");
    httpd_resp_sendstr(req, "{\"ok\":true,\"message\":\"Update successful, rebooting...\"}");

    vTaskDelay(pdMS_TO_TICKS(500));
    ESP.restart();
    return ESP_OK;
}

// --- Handler registration helpers (reduce startCameraServer size) ---

// Helper: register GET + OPTIONS pair for an endpoint
static void registerGetEndpoint(httpd_handle_t server, const char* uri,
                                 esp_err_t (*handler)(httpd_req_t*)) {
    httpd_uri_t get_uri = { .uri = uri, .method = HTTP_GET, .handler = handler, .user_ctx = NULL };
    httpd_uri_t opt_uri = { .uri = uri, .method = HTTP_OPTIONS, .handler = options_handler, .user_ctx = NULL };
    httpd_register_uri_handler(server, &get_uri);
    httpd_register_uri_handler(server, &opt_uri);
}

// Helper: register POST + OPTIONS pair
static void registerPostEndpoint(httpd_handle_t server, const char* uri,
                                  esp_err_t (*handler)(httpd_req_t*)) {
    httpd_uri_t post_uri = { .uri = uri, .method = HTTP_POST, .handler = handler, .user_ctx = NULL };
    httpd_uri_t opt_uri = { .uri = uri, .method = HTTP_OPTIONS, .handler = options_handler, .user_ctx = NULL };
    httpd_register_uri_handler(server, &post_uri);
    httpd_register_uri_handler(server, &opt_uri);
}

// Helper: register GET + POST + OPTIONS triple
static void registerCrudEndpoint(httpd_handle_t server, const char* uri,
                                  esp_err_t (*get_handler)(httpd_req_t*),
                                  esp_err_t (*post_handler)(httpd_req_t*)) {
    httpd_uri_t get_uri = { .uri = uri, .method = HTTP_GET, .handler = get_handler, .user_ctx = NULL };
    httpd_uri_t post_uri = { .uri = uri, .method = HTTP_POST, .handler = post_handler, .user_ctx = NULL };
    httpd_uri_t opt_uri = { .uri = uri, .method = HTTP_OPTIONS, .handler = options_handler, .user_ctx = NULL };
    httpd_register_uri_handler(server, &get_uri);
    httpd_register_uri_handler(server, &post_uri);
    httpd_register_uri_handler(server, &opt_uri);
}

// Helper: register GET + OPTIONS + HEAD triple (for stream endpoints)
static void registerStreamEndpoint(httpd_handle_t server, const char* uri,
                                    esp_err_t (*handler)(httpd_req_t*),
                                    esp_err_t (*head_handler)(httpd_req_t*)) {
    httpd_uri_t get_uri = { .uri = uri, .method = HTTP_GET, .handler = handler, .user_ctx = NULL };
    httpd_uri_t opt_uri = { .uri = uri, .method = HTTP_OPTIONS, .handler = options_handler, .user_ctx = NULL };
    httpd_uri_t head_uri = { .uri = uri, .method = HTTP_HEAD, .handler = head_handler, .user_ctx = NULL };
    httpd_register_uri_handler(server, &get_uri);
    httpd_register_uri_handler(server, &opt_uri);
    httpd_register_uri_handler(server, &head_uri);
}

void startCameraServer() {
    // Initialize SD Card (SPI mode — DFRobot FireBeetle2 DFR1154)
    // CS pin must be HIGH before SPI init (DFRobot example pattern)
    pinMode(SD_CARD_CS, OUTPUT);
    digitalWrite(SD_CARD_CS, HIGH);
    SPI.begin(SD_CARD_SCK, SD_CARD_MISO, SD_CARD_MOSI);
    SPI.setFrequency(1000000);  // 1MHz for reliable init
    if(!SD.begin(SD_CARD_CS, SPI, 4000000)) {
        Serial.println("❌ SD Card Mount Failed at 4MHz, retrying 1MHz...");
        if(!SD.begin(SD_CARD_CS, SPI, 1000000)) {
            Serial.println("❌ SD Card Mount Failed (no card or wiring issue)");
        }
    }
    if(SD.cardType() != CARD_NONE && SD.cardSize() > 0) {
        Serial.printf("✅ SD Card Mounted: %llu MB\n", SD.cardSize() / (1024 * 1024));
    }

    httpd_config_t http_config = HTTPD_DEFAULT_CONFIG();
    http_config.server_port = 80;
    http_config.ctrl_port = 32768;
    http_config.max_open_sockets = 3;         // REDUCED to save sockets (was 7)
    http_config.lru_purge_enable = true;      // Auto-close oldest socket when limit reached
    http_config.stack_size = 20480;           // FIXED: Increased from 16KB to 20KB to prevent stack overflow
    http_config.max_uri_handlers = 64;        // 61 handlers (GET/POST/OPTIONS per endpoint) + reserve

    httpd_config_t stream_config = HTTPD_DEFAULT_CONFIG();
    stream_config.server_port = 81;
    stream_config.ctrl_port = 32769;
    stream_config.max_open_sockets = 2;       // REDUCED (was 4) - Only 2 active streams allowed
    stream_config.lru_purge_enable = true;
    stream_config.stack_size = 20480;         // FIXED: Increased from 16KB to 20KB to prevent stack overflow
    stream_config.max_uri_handlers = 10;

    httpd_config_t audio_config = HTTPD_DEFAULT_CONFIG();
    audio_config.server_port = 82;
    audio_config.ctrl_port = 32770;
    audio_config.max_open_sockets = 2;        // REDUCED (was 4)
    audio_config.lru_purge_enable = true;
    audio_config.stack_size = 8192;           // Audio handler is simpler
    audio_config.max_uri_handlers = 5;

    Serial.println("🔧 Configuring HTTP servers...");

    // Initialize UDP Mutex
    if (udp_mutex == NULL) {
        udp_mutex = xSemaphoreCreateMutex();
        if (udp_mutex == NULL) {
             Serial.println("❌ Failed to create UDP mutex!");
        } else {
             Serial.println("✅ UDP mutex created");
        }
    }

    // Initialize recording mutex
    if (recordingMutex == NULL) {
        recordingMutex = xSemaphoreCreateMutex();
        if (recordingMutex == NULL) {
            Serial.println("❌ Failed to create recording mutex!");
        } else {
            Serial.println("✅ Recording mutex created");
        }
    }

    // FIXED: Initialize settings mutex (thread-safe) - NO configASSERT to avoid restart!
    if (settings_mutex == NULL) {
        settings_mutex = xSemaphoreCreateMutex();
        if (settings_mutex == NULL) {
            Serial.println("❌ Failed to create settings mutex!");
            // Don't restart - better to continue without mutex than restart loop
        } else {
            Serial.println("✅ Settings mutex created");
        }
    }

    Serial.println("Starting camera web server (Port 80)...");

    if (httpd_start(&camera_httpd, &http_config) == ESP_OK) {
        // --- Core endpoints ---
        registerGetEndpoint(camera_httpd, "/", index_handler);
        registerGetEndpoint(camera_httpd, "/frame", frame_handler);
        registerGetEndpoint(camera_httpd, "/status", status_handler);
        registerGetEndpoint(camera_httpd, "/health", health_handler);
        registerGetEndpoint(camera_httpd, "/psram-stats", psram_stats_handler);
        registerGetEndpoint(camera_httpd, "/stream-stats", stream_stats_handler);
        registerGetEndpoint(camera_httpd, "/motion-status", motion_status_handler);

        // Telemetry & debug
        registerGetEndpoint(camera_httpd, "/reg-debug", reg_debug_handler);
        registerGetEndpoint(camera_httpd, "/telemetry", telemetry_handler);

        // --- Settings ---
        registerGetEndpoint(camera_httpd, "/settings-page", settings_page_handler);
        registerCrudEndpoint(camera_httpd, "/settings", settings_get_handler, settings_post_handler);

        // --- IR control ---
        registerGetEndpoint(camera_httpd, "/ir-status", ir_status_handler);
        registerPostEndpoint(camera_httpd, "/ir-control", ir_control_handler);

        // --- Audio ---
        registerGetEndpoint(camera_httpd, "/audio-status", audio_status_handler);

        // --- Credentials & OTA ---
        registerCrudEndpoint(camera_httpd, "/credentials", credentials_get_handler, credentials_post_handler);
        registerPostEndpoint(camera_httpd, "/update", ota_update_handler);

        // --- Record ---
        registerPostEndpoint(camera_httpd, "/record", record_handler);

        // --- Captive portal (AP mode) ---
        registerGetEndpoint(camera_httpd, "/setup", captive_portal_handler);
        registerGetEndpoint(camera_httpd, "/wifi-scan", wifi_scan_handler);
        registerPostEndpoint(camera_httpd, "/wifi-save", wifi_save_handler);

        // Captive portal redirects
        registerGetEndpoint(camera_httpd, "/generate_204", captive_redirect_handler);
        registerGetEndpoint(camera_httpd, "/hotspot-detect.html", captive_redirect_handler);

        // --- Media & SD endpoints ---
        registerPostEndpoint(camera_httpd, "/save_frame", save_frame_handler);
        registerCrudEndpoint(camera_httpd, "/roi-mask", roi_mask_get_handler, roi_mask_post_handler);
        registerGetEndpoint(camera_httpd, "/motion-debug", motion_debug_handler);

        // SD card management
        registerGetEndpoint(camera_httpd, "/sd-list", sd_list_handler);
        registerGetEndpoint(camera_httpd, "/sd-download", sd_download_handler);
        registerPostEndpoint(camera_httpd, "/sd-clear", sd_clear_handler);

        // Live log endpoints
        wsLogRegisterHandlers(camera_httpd);

        Serial.println("All HTTP handlers registered");

    // Start Stream Server (Port 81)
    Serial.println("Starting stream server (Port 81)...");
    if (httpd_start(&stream_httpd, &stream_config) == ESP_OK) {
        registerStreamEndpoint(stream_httpd, "/stream", stream_handler, stream_head_handler);
        registerStreamEndpoint(stream_httpd, "/detection-stream", detection_stream_handler, stream_head_handler);

        Serial.println("✅ Stream server started successfully");
    } else {
        Serial.println("❌ Failed to start stream server");
    }

#ifndef LITE_MODE_NO_RUNTIME_TASKS
    // Start Audio Server (Port 82)
    Serial.println("Starting audio server (Port 82)...");
    if (httpd_start(&audio_httpd, &audio_config) == ESP_OK) {
        registerGetEndpoint(audio_httpd, "/audio.wav", audioStreamHandler);
    }

    // Start RTSP Server
    Serial.println("Starting RTSP server...");
    if (rtspServer.init()) {
        Serial.printf("✅ RTSP server started at rtsp://%s:554/mjpeg/1\n", WiFi.localIP().toString().c_str());
    } else {
        Serial.println("❌ Failed to start RTSP server");
    }
#else
    Serial.println("⚡ LITE_MODE: audio_httpd:82 (~8KB) + RTSP (~16KB) skipped");
#endif

    // Start Central Camera Capture Task (feeds both RTSP and MJPEG)
    Serial.println("Starting Camera Capture Task...");
    startCameraCaptureTask();

        Serial.println("Camera web server started successfully");

#ifndef LITE_MODE_NO_RUNTIME_TASKS
        // Start Recording Task (AVI with audio — needs extra stack for PCM buffer)
        xTaskCreatePinnedToCore(
            recordingTask,     // Task function
            "Recording",       // Task name
            6144,              // Stack size (3.2KB audio buffer on stack)
            NULL,              // Parameters
            1,                 // Priority (Low)
            NULL,              // Task handle
            1                  // Core 1 (same as capture to avoid cache thrashing?) or 0
        );
        Serial.println("✅ Recording task queued");
#else
        Serial.println("⚡ LITE_MODE: recording task skipped (~6KB stack)");
#endif
    } else {
        Serial.println("Failed to start camera web server");
    }
}
