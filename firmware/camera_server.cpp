// Handler: Single JPEG frame (reads from ring buffer -- no direct esp_camera_fb_get!)
static esp_err_t frame_handler(httpd_req_t *req) {
    int idx = getLatestFrameIndex();
    if (idx < 0) {
        Serial.println("Camera frame not available yet");
        httpd_resp_set_type(req, "application/json");
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "{\"error\":\"frame_not_available\"}");
        return ESP_FAIL;
    }

    if (!acquireFrameReader(idx)) {
        // Writer active, brief pause then retry
        vTaskDelay(pdMS_TO_TICKS(5));
        idx = getLatestFrameIndex();
        if (idx < 0 || !acquireFrameReader(idx)) {
            httpd_resp_set_type(req, "application/json");
            httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "{\"error\":\"frame_busy\"}");
            return ESP_FAIL;
        }
    }

    // Validate frame data
    if (frameBuffers[idx].data == NULL || frameBuffers[idx].len == 0) {
        releaseFrameReader(idx);
        Serial.println("Ring buffer frame empty");
        httpd_resp_set_type(req, "application/json");
        httpd_resp_send_err(req, HTTPD_500_INTERNAL_SERVER_ERROR, "{\"error\":\"frame_empty\"}");
        return ESP_FAIL;
    }

    httpd_resp_set_type(req, "image/jpeg");
    httpd_resp_set_hdr(req, "Content-Disposition", "inline; filename=capture.jpg");
    httpd_resp_set_hdr(req, "Access-Control-Allow-Origin", "*");

    esp_err_t res = httpd_resp_send(req, (const char *)frameBuffers[idx].data,
                                     frameBuffers[idx].len);

    releaseFrameReader(idx);
    return res;
}

